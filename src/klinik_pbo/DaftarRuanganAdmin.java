    private void ButtonEditRuanganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEditRuanganActionPerformed
        try {
            Connection conn = koneksi.getKoneksi();
            // Fetch rooms to display in a JComboBox for selection
            PreparedStatement psSelect = conn.prepareStatement("SELECT idRuangan, namaRuangan FROM ruangan");
            ResultSet rs = psSelect.executeQuery();

            JComboBox<String> comboBox = new JComboBox<>();
            Map<String, String> ruanganMap = new HashMap<>(); // To map name to ID

            while (rs.next()) {
                String id = rs.getString("idRuangan");
                String nama = rs.getString("namaRuangan");
                comboBox.addItem(nama);
                ruanganMap.put(nama, id);
            }
            rs.close();
            psSelect.close();

            if (ruanganMap.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tidak ada ruangan yang tersedia untuk diedit.");
                conn.close();
                return;
            }

            int option = JOptionPane.showConfirmDialog(this, comboBox, "Pilih Ruangan untuk Diedit", JOptionPane.OK_CANCEL_OPTION);
            if (option == JOptionPane.OK_OPTION) {
                String selectedNama = (String) comboBox.getSelectedItem();
                String selectedId = ruanganMap.get(selectedNama);

                // Fetch details of the selected room
                PreparedStatement psDetail = conn.prepareStatement("SELECT * FROM ruangan WHERE idRuangan = ?");
                psDetail.setString(1, selectedId);
                ResultSet detailRs = psDetail.executeQuery();

                if (detailRs.next()) {
                    String namaRuangan = detailRs.getString("namaRuangan");
                    int lantai = detailRs.getInt("lantai");
                    int kapasitas = detailRs.getInt("kapasitas");

                    JTextField namaField = new JTextField(namaRuangan);
                    JTextField lantaiField = new JTextField(String.valueOf(lantai));
                    JTextField kapasitasField = new JTextField(String.valueOf(kapasitas));

                    Object[] fields = {
                        "Nama Ruangan:", namaField,
                        "Lantai:", lantaiField,
                        "Kapasitas:", kapasitasField
                    };

                    int editOption = JOptionPane.showConfirmDialog(this, fields, "Edit Data Ruangan", JOptionPane.OK_CANCEL_OPTION);
                    if (editOption == JOptionPane.OK_OPTION) {
                        // Update the Ruangan object and then update in DAO
                        Ruangan updatedRuangan = new Ruangan(
                            selectedId,
                            namaField.getText(),
                            Integer.parseInt(lantaiField.getText()),
                            Integer.parseInt(kapasitasField.getText())
                        );
                        RuanganDAO.updateRuangan(updatedRuangan);
                        JOptionPane.showMessageDialog(this, "Data ruangan berhasil diperbarui.");
                        tampilkanDataRuangan(); // Refresh table
                    }
                }
                detailRs.close();
                psDetail.close();
            }
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat mengedit ruangan: " + e.getMessage());
            e.printStackTrace();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan lantai dan kapasitas dalam format angka yang benar.");
        }
    }//GEN-LAST:event_ButtonEditRuanganActionPerformed

    private void ButtonDeleteRuanganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonDeleteRuanganActionPerformed
        try {
            Connection conn = koneksi.getKoneksi();
            // Fetch rooms to display in a JComboBox for selection
            PreparedStatement psSelect = conn.prepareStatement("SELECT idRuangan, namaRuangan FROM ruangan");
            ResultSet rs = psSelect.executeQuery();

            JComboBox<String> comboBox = new JComboBox<>();
            Map<String, String> ruanganMap = new HashMap<>(); // To map name to ID

            while (rs.next()) {
                String id = rs.getString("idRuangan");
                String nama = rs.getString("namaRuangan");
                comboBox.addItem(nama);
                ruanganMap.put(nama, id);
            }
            rs.close();
            psSelect.close();

            if (ruanganMap.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tidak ada ruangan yang tersedia untuk dihapus.");
                conn.close();
                return;
            }

            int option = JOptionPane.showConfirmDialog(this, comboBox, "Pilih Ruangan untuk Dihapus", JOptionPane.OK_CANCEL_OPTION);
            if (option == JOptionPane.OK_OPTION) {
                String selectedNama = (String) comboBox.getSelectedItem();
                String selectedId = ruanganMap.get(selectedNama);

                int confirmDelete = JOptionPane.showConfirmDialog(this,
                    "Apakah Anda yakin ingin menghapus ruangan '" + selectedNama + "'?",
                    "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

                if (confirmDelete == JOptionPane.YES_OPTION) {
                    RuanganDAO.deleteRuangan(selectedId);
                    JOptionPane.showMessageDialog(this, "Ruangan berhasil dihapus.");
                    tampilkanDataRuangan(); // Refresh table
                }
            }
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus ruangan: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_ButtonDeleteRuanganActionPerformed

