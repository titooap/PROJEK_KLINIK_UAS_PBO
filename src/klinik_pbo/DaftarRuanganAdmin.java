    private void tampilkanDataRuangan() {
        DefaultTableModel model = new DefaultTableModel(
            new String[]{"ID Ruangan", "Nama Ruangan", "Lantai", "Kapasitas"},
            0
        );

        List<Ruangan> daftar = RuanganDAO.getAllRuangan();
        for (Ruangan r : daftar) {
            Object[] row = {
                r.getIdRuangan(),
                r.getNamaRuangan(),
                r.getLantai(),
                r.getKapasitas()
            };
            model.addRow(row);
        }
        TabelDaftarRuangan.setModel(model);
    }

